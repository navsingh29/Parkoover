package XMLParser;

public class Neighborhood {
	private String name;
	private String url;
	
	public Neighborhood() {
		super();
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getName() {
		return name;
	}
}
